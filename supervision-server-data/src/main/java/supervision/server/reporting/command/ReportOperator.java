package supervision.server.reporting.command;

public enum ReportOperator {

	/**
	 * EQUAL TO
	 */
	EQ,
	/**
	 * NOT EQUAL TO
	 */
	NEQ,
	/**
	 * GREATER THAN
	 */
	GT,
	/**
	 * LESS THAN
	 */
	LT,
	/**
	 * GREATER OR EQUAL
	 */
	GOE,
	/**
	 * LESS OR EQUAL
	 */
	LOE,
	/**
	 * LIKE (for string)
	 */
	LIKE

}
